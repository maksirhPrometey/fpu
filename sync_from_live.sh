#!/bin/sh
# ─────────────────────────────────────────────────────────────
#  Синхронізація даних із fpsu.org.ua → тестовий сайт
#  Запуск: ./sync_from_live.sh
# ─────────────────────────────────────────────────────────────
set -a
. /mnt_data/fpsu/.env
set +a
cd /mnt_data/fpsu

PY="./venv/bin/python manage.py"
SEP="──────────────────────────────────────────"

echo ""
echo "╔══════════════════════════════════════════╗"
echo "║   Синхронізація з fpsu.org.ua            ║"
echo "║   $(date '+%d.%m.%Y %H:%M')                       ║"
echo "╚══════════════════════════════════════════╝"
echo ""

# ── 1. Нові статті ──────────────────────────────────────────
echo "▶ [1/4] Нові статті..."
$PY import_new_articles --max-pages 20
echo $SEP

# ── 2. Оновлення порядку у напрямах діяльності ──────────────
echo "▶ [2/4] Синхронізація напрямів діяльності..."
$PY sync_menu_news_live
echo $SEP

# ── 3. Фото керівництва ─────────────────────────────────────
echo "▶ [3/4] Фото керівництва..."
$PY fetch_team_photos --force
echo $SEP

# ── 4. Членські організації ─────────────────────────────────
echo "▶ [4/4] Членські організації..."
$PY import_mem_orgs --update
echo $SEP

echo ""
echo "✓ Синхронізацію завершено: $(date '+%d.%m.%Y %H:%M')"
echo ""
